def to_hex(s):
    result = ''
    for b in s.encode('utf-8'):
        if 0x20 <= b <= 0x7e and b != ord('"') and b != ord('\\'):
            result += chr(b)
        else:
            result += '\\x{:02x}'.format(b)
    return result

